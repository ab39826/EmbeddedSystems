//*************RIT128x96x4_ShowImage************************************** 
// Writes the specified string to the RAM version of the image.
// Based upon rit128x96x4.c's WriteString function
// Inputs: x, y are offsets from bottom left, brightness is 0-15 
// Outputs: none
void RIT128x96x4_DrawString(const char* string, int x, int y, unsigned char brightness){

	//
    // Loop while there are more characters in the string.
    //
	unsigned char ucTemp;
	int xIdx;
	int yIdx;
	unsigned char workingCol;
    while(*string != 0)
    {
        //
        // Get a working copy of the current character and convert to an
        // index into the character bit-map array.
        //
        ucTemp = *string++ & 0x7f;
        if(ucTemp < ' ')
        {
            ucTemp = 0;
        }
        else
        {
            ucTemp -= ' ';
        }

        //
        // Display the character, high bit is bottom, low bit is top.
        //
		
        for(xIdx = 0; xIdx < 5; xIdx++)
        {
            //
            // Display a column of 1-bit font data
            //
			
			workingCol = g_pucFont[ucTemp][xIdx];
			
            for(yIdx = 0; yIdx < 8; yIdx++)
            {
                if (workingCol & 0x80) {
					setPixel(x, y + yIdx, brightness);
				}
				workingCol = workingCol << 1;
            }
			
			x++;

            if(x == 128)
            {
                return;
            }
        }
		
		x++;
    }

}
	
